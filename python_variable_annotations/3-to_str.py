#!/usr/bin/env python3
"""
This module contains a type-annotated function 'to_str'
that converts a float to a string.
"""


def to_str(n: float) -> str:
    """
    Converts a float n to its string representation.

    Parameters:
    n (float): The float number to be converted to a string.

    Returns:
    str: The string representation of the float n.
    """
    return str(n)
